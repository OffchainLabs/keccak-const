fn combine<const N: usize, const M: usize, const S: usize>(a: [u8; N], b: [u8; M]) -> [u8; S] {
    let mut both = [0; S];
    both[..N].copy_from_slice(&a);
    both[N..].copy_from_slice(&b);
    both
}
